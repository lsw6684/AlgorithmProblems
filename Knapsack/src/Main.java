/*
Knapsack
Description

무게와 가치가 서로 다른 N개의 물건들이 존재한다. W만큼의 무게를 담을 수 있는 한 개의 가방에 이 물건들을 담는다고 할 때, 가방에 담긴 물건들의 가치의 총 합의 최대치를 구하는 프로그램을 작성하시오.


Input
첫 번째 줄에는 물건의 수 N과 가방에 담을 수 있는 최대 무게 W가 주어진다. (N < 100, W < 10000)

두 번째 줄부터 각 물건들의 가치(vi)와 무게(wi)가 순서대로 주어진다. (0 < vi < 100, 0 < wi < 100)


Output
가방에 담을 수 있는 물건들의 가치의 총 합을 출력한다.


Sample Input 1 

4 10
10 5
40 4
30 6
50 3
Sample Output 1

90
--------------------------------------
*/